CREATE DATABASE usm;
USE usm;

CREATE TABLE Jalur_USM (
Kode_Jalur VARCHAR(10), 
Ket VARCHAR(32), 
StartYear INT, 
EndYear INT,
PRIMARY KEY(Kode_Jalur)
);
 

CREATE TABLE Sekolah(
NamaSekolah VARCHAR(38), 
Alamat VARCHAR(50), 
Kabupaten VARCHAR(21), 
Propinsi VARCHAR(8), 
NoTelepon VARCHAR(20), 
ContactPerson VARCHAR(13),
PRIMARY KEY (NamaSekolah)
);


CREATE TABLE Pendaftar (
NoPendaftar VARCHAR(11), 
Nama VARCHAR(19), 
TempatLahir VARCHAR(16), 
Tanggallahir DATE, 
JenisKelamin VARCHAR(12), 
Agama VARCHAR(9), 
Alamat VARCHAR(89), 
NoTelepon VARCHAR(31), 
NamaAyah VARCHAR(23), 
NamaIbu VARCHAR(22), 
AsalSekolah VARCHAR(38), 
JalurUSM VARCHAR(8),
PRIMARY KEY(NoPendaftar),
FOREIGN KEY (JalurUSM) REFERENCES Jalur_USM(kode_jalur),
FOREIGN KEY (AsalSekolah) REFERENCES Sekolah(NamaSekolah)
);


 CREATE TABLE MataPelajaran_USM (
 Kode_MP VARCHAR(10), 
 Ket VARCHAR(16),
 PRIMARY KEY(Kode_MP)
 );



CREATE TABLE HasilUSM_Akademis(
NoPendaftar VARCHAR(11), 
MataPelajaran VARCHAR(13), 
Nilai FLOAT,
PRIMARY KEY(NoPendaftar,MataPelajaran),
FOREIGN KEY(NoPendaftar)REFERENCES Pendaftar(NoPendaftar),
FOREIGN KEY (MataPelajaran)REFERENCES MataPelajaran_USM(Kode_MP)
);


 CREATE TABLE History_Ranking (
 NoPendaftar VARCHAR(11), 
 Semester VARCHAR(8), 
 Kelas VARCHAR(5), 
 RankingKelas INT, 
 JumlahSiswa INT,
 PRIMARY KEY(NoPendaftar,Semester,Kelas),
 FOREIGN KEY(NoPendaftar)REFERENCES Pendaftar(NoPendaftar)
 );
 

  CREATE TABLE History_NilaiRaport (
  NoPendaftar VARCHAR(11), 
  Semester VARCHAR(8), 
  Kelas VARCHAR(5), 
  MataPelajaran VARCHAR(13), 
  Nilai FLOAT,
  PRIMARY KEY(NoPendaftar,Semester,MataPelajaran,Kelas),
  FOREIGN KEY(NoPendaftar)REFERENCES Pendaftar(NoPendaftar)
 );
   
